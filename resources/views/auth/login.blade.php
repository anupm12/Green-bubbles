<script>
    $(".toggle-password").click(function() {
        var input = $('#password');
        if (input.attr("type") == "password") {
            input.attr("type", "text");
            $(this).empty();
            $(this).append('<ion-icon name="eye-off"></ion-icon>');
        } else {
            input.attr("type", "password");
            $(this).empty();
            $(this).append('<ion-icon name="eye"></ion-icon>');
        }

    });
</script>
